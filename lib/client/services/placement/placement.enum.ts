/**
 * Represents the status of objects that represent inventory - ad units and placements.
 */
export enum InventoryStatus {
  /**
   * The object is active.
   */
  ACTIVE = "ACTIVE",
  /**
   * The object is no longer active.
   */
  INACTIVE = "INACTIVE",
  /**
   * The object has been archived.
   */
  ARCHIVED = "ARCHIVED",
}
